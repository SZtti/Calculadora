
public class Aluno {
	private String Nome;
	private String Matricula;
	private int Curso;
	private int Turno;
	
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getMatricula() {
		return Matricula;
	}
	public void setMatricula(String matricula) {
		Matricula = matricula;
	}
	public int getCurso() {
		return Curso;
	}
	public void setCurso(int curso) {
		Curso = curso;
	}
	public int getTurno() {
		return Turno;
	}
	public void setTurno(int turno) {
		Turno = turno;
	}
	
	
}
